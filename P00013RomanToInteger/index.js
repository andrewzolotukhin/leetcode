/**
 * @param {s} string
 * @return {number}
 */
var romanToInt = function(s) {
	const knownValues = { 
		1: 'I', 2: 'II', 3: 'III', 4: 'IV', 5: 'V', 6: 'VI', 7: 'VII', 8: 'VIII', 9: 'IX', 10: 'X', 11: 'XI', 20: 'XX', 30: 'XXX', 40: 'XL', 50: 'L',
		17: 'XVII', 90: 'XC', 400: 'CD', 900: 'CM', 
		100: 'C', 500: 'D', 1000: 'M'
	}
	const tokens = [ {r: 'CM', n: 900}, {r: 'CD', n: 400}, {r: 'XC', n: 90}, {r: 'XL', n: 40}, { r: 'IX', n: 9}, { r: 'IV', n: 4}, { r: 'I', n: 1}, { r: 'V', n: 5}, { r: 'X', n: 10}, {r :'L', n: 50}, { r: 'C', n: 100 }, { r: 'D', n: 500}, { r: 'M', n: 1000} ];

	let result = 0;

	const getToken = (is) => { if(!is) return null; for(let i = 0; i < tokens.length; i++) { if(is.startsWith(tokens[i].r)) return tokens[i]; } }

	let token;

	while (token = getToken(s)) {
		result += token.n;
		s = s.replace(token.r, '');
	}

	return result;
};

