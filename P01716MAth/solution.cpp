class Solution {
public:
  int totalMoney(int n) {
    // 1 2 3 4 5 6 7 -> (1+7)*7/2 = 28
    // 2 3 4 5 6 7 8 -> (2+8)*7/2 = 35 = 28 + 7
    // 3 4 5 6 7 8 9 -> (3+9)*7/2 = 42 = 28 + 7*2
    // ...............
    //                  (w+w+7)*7/2 = 28 + 7*(w-1)

    // w*28 + 7 + 7*2 + 7*3 ... 7*(w-1) = 28*w + 7*(1 + 2 + 3 + ... (w-1)) =
    // = 28*w + 7* (1 + (w-1))*(w-1)/2

    int d = n % 7;
    int w = n / 7;
    return 28 * w + 7 * (1 + (w - 1)) * (w - 1) / 2 + (2 * w + 1 + d) * d / 2;
  }
};
